<legend>What is C2P?</legend> 
<p><b>Connecting2People</b> is a semi-automated returnee referral system designed to help; 
<ol>
   <li>Students who have found faith while studying overseas, to connect to people who share their Christian faith back home.</li>
   <li>Students who move from place to place, while overseas.</li>
</ol>
   The Internet is not a secure place to store personal information, especially 
    about people in, or returning to, countries where people are often 
    persecuted for their beliefs, so <i style="color:red;">this web site does not hold 
    any information about people in, or returning to, such countries</i>. It is 
    simply a semi-automated referral system that allows people who have friends 
    returning home to network with other people who already have friends in such
    countries. </p>
