<h4>Are you a Returnee?</h4>
<p>If you have been working or studying overseas, have become a Christian, and 
    will be returning home in the near future, then we would like to help you 
    to connect back home with people who share your faith. 
    For security reasons we do not want to have your contact details
    in our system, and therefore in order to assist you, you will need to  
    a friend who lives outside your home country, to act as your 
    <a href="../c2p/forsponsors">sponsor</a> and point 
    of contact. Please ask your friend to register on this web-site as member. 
    They will then be able to make a returnee referral enquiry on your behalf. 
    If a contact is found for you back home, it will be your sponsor who will 
    pass that information to you and follow-up your progress.</p>
