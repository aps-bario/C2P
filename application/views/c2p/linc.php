<h4>Connecting2People partnership with Linc Ministries</h4>
<p>C2P partner with a a variety of agencies that share the same vision and goals.
    One of these is LINC Ministries and therefore a custom mobile friendly front-end
    has been created that allows those familiar with the LINC ministries name to 
    be able to easily access the core features of the C2P system. This includes;</p>
<ul>
    <li>A custom URL that can be used to redirect users from another website</li>
    <li>A custom HOME page, with a LINC heading and welcome message</li>
    <li>Access to selected functionality to allows including:
        <ul>
            <li>Member registration</li>
            <li>Entry of references</li>
            <li>Editing own member data</li>
            <li>Submitting Returnee referral requests</li>
            <li>Checking referral progress</li> 
        </ul>
    </li>
</ul>    
<p>LINC Ministires Mobile URL -
<a href="../linc/home">https://www.connecting2people.net/linc/home</a></p>
<p>It is assumed that one a member gains higher level access within the C2P 
    membership network, they will be fully aware of the association with 
    Connecting2People and therefore happy to use the main web site for looking-up 
    churches, managing their referrals as a Gatekeeper, viewing reports etc.</p>
<p>These simplified interfaces also work well in a normal browser so you may find
    that you prefer to use them most of the time. If you have higher level access
    privileges then once you have logged in, there will be a link on the menu to 
    access the main C2P web site (an you will already be logged in). 
</p>
