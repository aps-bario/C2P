<h4>How to access C2P from a mobile device</h4>
<p>There are a lot of complex pages in the main C2P web site that will not render 
    well on a mobile phone and some tablet devices. Also when away from you computer
    you do not always need all the functionality this web site provides. You 
    probably only want to login and add a new returnee while they are there with 
    you, so that you can get their home location correct. Alternatively, you may 
    simply want to give a potential new sponsor, a simple web address where they 
    can register as a member, and enter enter there references.</p>
<p>So we have created a couple of mobile friendly web front-ends to 
    the C2P website that provide a quick and simple interface.</p>
<p>For C2P mobile users use the address - 
    <a href="../mobile/home">https://www.connecting2people.net/mobile/home</a></p>
<p>Or for partner organisations like Linc Ministires, a custom mobile interface -
<a href="../linc/home">https://www.connecting2people.net/linc/home</a></p>
<p>These simplified interfaces also work well in a normal browser so you may find
    that you prefer to use them most of the time. If you have higher level access
    privileges then once you have logged in, there will be a link on the menu to 
    access the main C2P web site (an you will already be logged in). 
</p>
