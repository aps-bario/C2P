<H4>Statement of Faith</h4>
<p>
This web-site is operated by and for the benefit of Christians who share a 
common faith based on be the fundamental truths of Christianity, 
as revealed in the Bible, including:</br>
<Style>
ol {type:'a'; start:'a';}
ol.li {margin: 5px; }
</style>
<ul>
    <li>There is one God in three persons, the Father, the Son and the Holy Spirit.</li>
    <li>God is sovereign in creation, revelation, redemption and final judgement.</li>
    <li>The Bible, as originally given, is the inspired and infallible Word of God. 
        It is the supreme authority in all matters of belief and behaviour.</li>
    <li>Since the fall, the whole of humankind is sinful and guilty, 
        so that everyone is subject to God's wrath and condemnation.</li>
    <li>The Lord Jesus Christ, God's incarnate Son, is fully God; he was born of a virgin; 
        his humanity is real and sinless; he died on the cross, 
        was raised bodily from death and is now reigning over heaven and earth.</li>
    <li>Sinful human beings are redeemed from the guilt, 
        penalty and power of sin only through the sacrificial death once 
        and for all time of their representative and substitute, 
        Jesus Christ, the only mediator between them and God.</li>
    <li>The Lord Jesus Christ will return in person, to judge everyone, 
        to execute God's just condemnation on those who have not repented 
        and to receive the redeemed to eternal glory, 
        the sovereignty of God in creation, revelation, 
        redemption and final judgement;</li>
    <li>Those who believe in Christ are pardoned all their sins 
        and accepted in God's sight only because of the righteousness of Christ credited to them; 
        this justification is God's act of undeserved mercy, 
        received solely by trust in him and not by their own efforts.</li>
    <li>The one holy universal church is the Body of Christ, 
        to which all true believers belong.</li>
    <li>The Holy Spirit alone makes the work of Christ effective to individual sinners, 
        enabling them to turn to God from their sin and to trust in Jesus Christ.</li>
    <li>The Holy Spirit lives in all those he has regenerated. 
        He makes them increasingly Christlike in character and behaviour 
        and gives them power for their witness in the world.</li>   
</ul>
</p>
