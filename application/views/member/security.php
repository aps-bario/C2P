<H4>Security Warning</h4>
<p>
    Every effort has been made to make this web-site and returnee referral system 
    as secure as it can be. All users of the system are verified and needed a 
    ministry reference before they joined. We also have made a point of not holding
    any contact details for people in, or returning to China. 
    <b>However</b>, the weakest link is in personal communications, so please 
    remember the following security guidelines, especially when corresponding with 
    people in the country that returnees call home.</P>
<ul>
    <li>
        <b>No electronic communication is completely secure, so always think before 
                you say or type anything.</b></li>
    <li>
        Avoid using key words and phases that might draw attention to your correspondence 
        e.g. 'Christian', 'Jesus', 'church', baptism', 'conversions' etc. Most of these
        words can be avoided using substitutes that make sense in context but are 
        less likely to be flagged by security agencies.
        
        For example, "<i>It was good to hear that you are meeting regularly with the family, 
        and that you friend was washed. Do keep talking to Father about the issues you
        you mentioned and re will be remembering your situation here also.</i>" 
        
        It is probably a good idea to avoid obvious code words as that just makes
        it look as if you are hiding things</li>
    <li>
        Avoid passing detailed locations or personal contact details, where this is 
        necessary perhaps refer to them in one message and provide them with no comment
        in another.</li>
    <li>
        Social media like WeChat may be the only way to make contact with some people,
        but it is not secure or encrypted. So if you need to discuss or transfer 
        sensitive information, use an alternative. Some systems are better than others, 
        some of the best like secure email or a VPN are however often blocked 
        and too much use of such systems may also attract unwanted attention.</li>
    <li>
        C2P communications contain a link that quickly allows you to flag concerns about
        another member of the network. Clicking the 'Concerns' link with suspend the other 
        persons account until the issue has been investigated by the Admin team. 
        In the first instance, please express your concerns to the person involved, but
        do use this feature if you believe the individual is being careless about security 
        or you believe they are not the person they claim to be. </li>
</ul>
<p>If you would like to add or comment these guidelines please use the 
    <a href="../c2p/feedback">feedback</a> page.</p>    
