<h4>Those wanting to help friends returning to China (Sponsors)</h4>
<p>If you know a Chinese Christian who will be returning to China in the near future, 
    or who has returned recently, and who is wanting to find find fellowship in the 
    area to which they are returning, then we would like to help you to help them.</p>
<p>It has proven much more difficult for those returning to make contact with those 
    of the same faith and like mind, but they are not alone and thousands of others 
    have returned and settled well into churches and fellowship groups.</p>
<p>This is a simple system that attempts to link people who have friends returning 
    with others who already know people who have, and who are willing to assist others
    like themselves.</p>
<p>Because is it important to verify the identity of those who are being connected, 
    each person needs a Christian friend to act as their sponsor, and make enquires 
    on their behalf. They themselves also need to be able to provide a reference to 
    assure those in country that those being referred to them are who they say they are. 
</p>The first step in becoming a recognised sponsor is to register as a member 
<a href="../linc/newmember">here</a>. </p>
