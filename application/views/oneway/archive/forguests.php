<h4>FOR GUESTS (Any International Student wanting to meet local people)</h4>
<h4>What we do</h4>
<p>The purpose of this web-site is to give International Students studying in 
   Coventry and Warwickshire the opportunity to get to know local people. All 
   our hosts are members of local churches who have been verified as good 
   people to offer hospitality to students from around the world.</p>
<p>International Students are asked to register their contact details and 
   basic preferences about the type of invitation they would be comfortable with. 
   This provides provides a general profile that can be used to match guests with 
   hosts, who will also have to have completed a similar profile.</P>
<p>In addition to the profile, students are also encouraged to submit a request 
   for each particular type of invitation they would like to receive. This will 
   in future allow hosts to respond to these requests inviting students to the 
   types of events that they are most interested in.</p>
<p>Likewise, hosts will in future be able to publicise open events that students
   will be able to view and invite themselves to. </p>
<p><b>PLEASE NOTE</b> in the lead up to Christmas, the main focus is on making 
   sure that as many people as possible are offered the opportunity to be invited 
   to Christmas Dinner, and therefore other options may only available later in 
   the year</P>
<h4>What you need to do</h4>
<p>If this is your first visit to this web-site, then you will need to register.  
   Select the Registration option from the left menu to register 
   as a GUEST. You will then be sent an email to confirm your email address.</p>
<p>The email you will be sent will contain a link that will bring you back to 
   this web-site, and will confirm your registration.</p>
<p>Once your registration is confirmed you will be able to login. You will then 
   find a new menu at the top of the page, next to <b>Home</b> called <b>Guest</b>. 
   Clicking on <b>Guest</b> will provide a new menu on the left-hand side of the page.</P>
<p>Please click <b>My Profile</b> and enter/update your contact details, and also
   select your preferences about the types of events you would like to be invited to.</b>
<p><b>PLEASE NOTE</b> there are at present a limited number of hosts and therefore
   specifying your preferences is not a guarantee that we will be able to find a 
   matching host, so the more options you pick and few restrictions, will make it
   easier to find you an appropriate invitation.</p>
<p>Once we have allocated you to a host event, we will send you an email 
   confirming the details of the event.  You will then also be contacted by your host.
</p>

