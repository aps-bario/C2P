<h4>HOSTS</h4>
<h4>How it works</h4>
<p>There are a few of stages to registering as a host with CWISW, and therefore each time you return 
   to this web-site more options will be available to you.</P>
<p>1. Register as a user of the web-site by selecting the Registration option from the left hand menu. 
   At the bottom of the New User page select the option Host or Helper.  
   You will be sent an email to confirm your email address.</p>
<p>2. When you receive the confirmation email, click the link in the message and it will return you to 
   the web-site and allow you to login.</p>
<p>3. If you registered as a Helper, then a new menu option called 'Helper' will appear top right next to 'Login'. 
   Click on 'Helper' and the menu on the left hand side will change. Please select 'My Profile' to provide us 
   with basic information that will help us link you with students. If you only want to be a Helper then only the top 
   part of this form is needed. However, to be a host you will be required to complete the entire form 
   and submit it using the button at the bottom of the page. Completion of the full profile form will include details 
   of a church reference. </p>
<p>After you have registered, you will be able to find out about events that others are hosting, 
and will be able to help them if you wish. Helpers can stay as helpers and join in with events 
by signing up as a guest and liaising with the person hosting an event.</p>
<p>If you want to be able to host your own events, we will need to ask your church leader to 
confirm that you are suitable to host international students.</p>
<p>4. Once we have received this reference, we will upgrade you to a HOST and and a new 'Host' option will 
   appear top right. The Host menu, will allow you to edit you Profile, but also create events to which 
   students can be invited. This will enable you to plan to host international students 
on a day and time that suits you.</p>
